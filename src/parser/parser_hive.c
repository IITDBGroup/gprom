static Node *parseInternalHive (void);

Node *
parseStreamHive (FILE *stream)
{
    hivein = stream;

    return parseInternalHive();
}

Node *
parseFromStringHive (char *input)
{
    INFO_LOG("parse SQL:\n%s", input);
    hiveSetupStringInput(input);

    return parseInternalHive();
}

static Node *
parseInternalHive (void) //TODO make copyObject work first
{
    Node *result;
    START_TIMER("module - parser");

    NEW_AND_ACQUIRE_MEMCONTEXT("PARSER_CONTEXT");

    // parse
    int rc = hiveparse();
    if (rc)
    {
        ERROR_LOG("parse error!");
        return NULL;
    }

    STOP_TIMER("module - parser");

    DEBUG_NODE_BEATIFY_LOG("query block model generated by parser is:",hiveParseResult);

    // create copy of parse result in parent context
    FREE_MEM_CONTEXT_AND_RETURN_COPY(Node,hiveParseResult);
}
