SELECT sum((F0.L_EP * F0.L_D)) AS A0
FROM (
SELECT F0.L_ORDERKEY AS L1, F0.L_PARTKEY AS L2, F0.L_SUPPKEY AS L3, F0.L_LINENUMBER AS L4, F0.L_QUANTITY AS L_Q, F0.L_EXTENDEDPRICE AS L_EP, F0.L_DISCOUNT AS L_D, F0.L_TAX AS L5, F0.L_RETURNFLAG AS L6, F0.L_LINESTATUS AS L7, F0.L_SHIPDATE AS L_SD, F0.L_COMMITDATE AS L8, F0.L_RECEIPTDATE AS L9, F0.L_SHIPINSTRUCT AS L10, F0.L_SHIPMODE AS L11, F0.L_COMMENT AS L12
FROM LINEITEM AS F0) F0
 WHERE (((((F0.L_SD >= '1994-01-01') AND (F0.L_SD < '1995-01-01')) AND (F0.L_D >= 0.050000)) AND (F0.L_D <= 0.070000)) AND (F0.L_Q < CAST (24 AS DOUBLE)))
